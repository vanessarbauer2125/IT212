public class AudioPlayer implements MediaPlayer {
   // Plays the video
   public void play() {
      System.out.println("Audio Player is playing");
   }

   // Stops the video
   public void stop(){
      System.out.println("Audio player has stopped");
   };

   // Pauses the video
   public void pause(){
      System.out.println("Audio player has paused");
   };

}