public class StreamingPlayer implements MediaPlayer {

   // Plays the stream
   public void play() {
      System.out.println("Streaming Player is playing");
   }

   // Stops the stream
   public void stop(){
      System.out.println("Streaming player has stopped");
   };

   // Pauses the stream
   public void pause(){
      System.out.println("Streaming player has paused");
   };

}