public class VideoPlayer implements MediaPlayer {
   // Plays the video
   public void play() {
      System.out.println("Video player is playing");
   }

   // Stops the video
   public void stop(){
      System.out.println("Video player has stopped");
   };

   // Pauses the video
   public void pause(){
      System.out.println("Video player has paused");
   };

}